struct Range {
  char32_t lo;
  char32_t hi;
};

static bool InRanges(char32_t cp, const Range *ranges, std::size_t count) {
  for (std::size_t i = 0; i < count; ++i) {
    if (cp >= ranges[i].lo && cp <= ranges[i].hi) {
      return true;
    }
  }
  return false;
}

